import json, subprocess, sys, os
from pathlib import Path
from datetime import datetime
from typing import Any

ROOT = Path(__file__).resolve().parents[1]
SANDBOX = ROOT / "sandbox" / "generated_scripts"


def root() -> Path:
    return ROOT


def sandbox() -> Path:
    SANDBOX.mkdir(parents=True, exist_ok=True)
    return SANDBOX


def plan_script(goal: str) -> dict[str, Any]:
    """Plan a script based on a natural-language goal."""
    goal_lower = goal.lower()

    if "report" in goal_lower or "status" in goal_lower:
        plan = {
            "goal": goal,
            "script_name": "generate_status_report.py",
            "description": "Generates a consolidated status report from all report files",
            "inputs": ["reports/"],
            "output": f"reports/{datetime.now().strftime('%Y%m%d')}_consolidated_report.json",
            "steps": ["Read all JSON files from reports/", "Merge into single report", "Write to output"],
            "safe": True,
        }
    elif "memory" in goal_lower and "count" in goal_lower:
        plan = {
            "goal": goal,
            "script_name": "count_memory_items.py",
            "description": "Counts all items in smart memory stores",
            "inputs": ["data/smart_memory/"],
            "output": "reports/memory_counts_report.json",
            "steps": ["Read each JSONL file", "Count lines", "Produce summary JSON"],
            "safe": True,
        }
    elif "test" in goal_lower or "check" in goal_lower:
        plan = {
            "goal": goal,
            "script_name": "run_safety_checks.py",
            "description": "Runs all safety checks and reports",
            "inputs": ["src/", "scripts/"],
            "output": "reports/safety_check_report.json",
            "steps": ["Import safety modules", "Run checks", "Produce report"],
            "safe": True,
        }
    else:
        plan = {
            "goal": goal,
            "script_name": f"script_{datetime.now().strftime('%Y%m%d_%H%M%S')}.py",
            "description": f"Custom script for: {goal[:60]}",
            "inputs": [],
            "output": f"reports/{datetime.now().strftime('%Y%m%d')}_custom_output.json",
            "steps": ["Parse goal", "Execute safe operation", "Write result"],
            "safe": True,
        }

    return plan


