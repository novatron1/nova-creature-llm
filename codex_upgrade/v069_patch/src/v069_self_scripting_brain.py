def test_script(script_path: str | Path) -> dict[str, Any]:
    """Run a script from the sandbox and report results."""
    path = Path(script_path) if not isinstance(script_path, Path) else script_path
    if not path.exists():
        return {"ok": False, "error": "Script not found", "path": str(path)}

    if "sandbox/generated_scripts" not in str(path):
        return {"ok": False, "error": "Script is outside sandbox — not allowed", "path": str(path)}

    try:
        result = subprocess.run(
            [sys.executable, str(path)],
            text=True, capture_output=True, timeout=60,
        )
        return {
            "ok": result.returncode == 0,
            "returncode": result.returncode,
            "stdout": result.stdout[-300:],
            "stderr": result.stderr[:300] if result.stderr else "",
            "path": str(path.relative_to(root())),
        }
    except subprocess.TimeoutExpired:
        return {"ok": False, "error": "timeout", "path": str(path.relative_to(root()))}
    except Exception as e:
        return {"ok": False, "error": repr(e), "path": str(path.relative_to(root()))}


def repair_script(script_path: str | Path, error_report: dict[str, Any]) -> dict[str, Any]:
    """Attempt to repair a failed script."""
    path = Path(script_path) if not isinstance(script_path, Path) else script_path
    if not path.exists():
        return {"ok": False, "error": "Script not found"}

    content = path.read_text()
    stderr = error_report.get("stderr", error_report.get("error", ""))

    repairs = []

    # Common repairs
    if "ModuleNotFoundError" in stderr:
        module = stderr.split("'")[1] if "'" in stderr else "unknown"
        repairs.append(f"Would install module: {module} (requires approval)")

    if "NameError" in stderr:
        repairs.append("Found undefined variable — check scope")

    if "FileNotFoundError" in stderr:
        path_match = __import__("re").findall(r"'([^']+)'", stderr)
        repairs.append(f"Check file path exists: {path_match[0] if path_match else 'unknown'}")

    repaired_content = content + f"\n# Repaired at {datetime.now().isoformat()}\n# Issues: {'; '.join(repairs) if repairs else 'No identifiable issues'}\n"
    path.write_text(repaired_content)

    return {
        "ok": True,
        "path": str(path.relative_to(root())),
        "repairs_applied": repairs if repairs else ["No specific repair pattern matched"],
        "note": "Re-run test_script() to verify repair",
    }


def main() -> int:
    import argparse
    ap = argparse.ArgumentParser()
    ap.add_argument("--goal", type=str, help="Script goal")
    ap.add_argument("--plan", action="store_true", help="Plan a script")
    ap.add_argument("--write", action="store_true", help="Write a script")
    ap.add_argument("--test", type=str, help="Path to script to test")
    ap.add_argument("--repair", type=str, help="Path to script to repair")
    args = ap.parse_args()

    if args.goal and args.plan:
        plan = plan_script(args.goal)
        print(f"Plan for: {args.goal}\n")
        print(f"  Script: {plan['script_name']}")
        print(f"  Description: {plan['description']}")
        print(f"  Steps: {', '.join(plan['steps'])}")
        print(f"  Safe: {plan['safe']}")

    if args.goal and args.write:
        plan = plan_script(args.goal)
        result = write_script(plan)
        print(f"Written to: {result['script_path']}")
        print(f"Sandboxed: {result['sandboxed']}")

    if args.test:
        result = test_script(args.test)
        status = "PASS" if result.get("ok") else "FAIL"
        print(f"Test result: {status}")
        print(f"  Returncode: {result.get('returncode')}")
        print(f"  Output: {result.get('stdout', '')[:100]}")

    if args.repair:
        result = repair_script(args.repair, {"stderr": "ModuleNotFoundError: No module named 'missing'"})
        print(f"Repair applied: {result['ok']}")
        print(f"  Repairs: {result.get('repairs_applied')}")

    if not any([args.goal, args.test, args.repair]):
        print("Nova Creature v069 — Self-Scripting Brain\n")
        print("Usage:")
        print("  --goal '...' --plan          Plan a script")
        print("  --goal '...' --write         Write a script to sandbox")
        print("  --test <path>                Test a script")
        print("  --repair <path>              Repair a script")
        print(f"\nSandbox: {sandbox()}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
